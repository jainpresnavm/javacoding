package week1.day2;

public class FibonacciSeries {

	public static void main(String[] args) {
		int range=8;
		int first=0;
		int second=1;
		int next=0;
		
		
		//program output-0,1,1,2,3,5,8 and 13
for (int i = 0; i < range; i++) {
	System.out.println(first +"");
	next=first+second;
	first=second;
	second=next;

	
}
	}

}
